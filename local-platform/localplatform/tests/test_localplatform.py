class TempDirTest(unittest.TestCase):
    def setUp(self) -> None:
        self._tmp = tempfile.TemporaryDirectory()
        self.tmp = Path(self._tmp.name)
        self.addCleanup(self._tmp.cleanup)

    def write(self, name: str, content: str) -> Path:
        path = self.tmp / name
        path.write_text(content)
        return path


class TestEnvFileScanner(TempDirTest):
    def parse(self, content: str, base: dict[str, str] | None = None):
        return parse_env_file(self.write("t.env", content), base_env=base or {})

    def test_generated_test_env_shape(self) -> None:
        parsed = self.parse(
            "#!/usr/bin/env bash\n"
            "# Generated by smbe local-dev-env.\n"
            'export WASMER_REGISTRY="http://backend:8000/graphql"\n'
            'export WASMER_TOKEN="wap_abc123"\n'
            "\n"
            'export EDGE_SERVER="http://127.0.0.1:19080"\n'
        )
        self.assertEqual(
            parsed,
            {
                "WASMER_REGISTRY": "http://backend:8000/graphql",
                "WASMER_TOKEN": "wap_abc123",
                "EDGE_SERVER": "http://127.0.0.1:19080",
            },
        )

    def test_quote_semantics(self) -> None:
        parsed = self.parse(
            "export BASE=hello\n"
            'export DOUBLE="$BASE world"\n'
            "export UNQUOTED=$BASE-suffix\n"
            "export SINGLE='$BASE stays'\n"
            'export BRACED="${BASE}_x"\n'
            'export FROM_CALLER="$CALLER!"\n'
            'export MISSING="$NOPE-"\n',
            base={"CALLER": "outer"},
        )
        self.assertEqual(parsed["DOUBLE"], "hello world")
        self.assertEqual(parsed["UNQUOTED"], "hello-suffix")
        self.assertEqual(parsed["SINGLE"], "$BASE stays")
        self.assertEqual(parsed["BRACED"], "hello_x")
        self.assertEqual(parsed["FROM_CALLER"], "outer!")
        self.assertEqual(parsed["MISSING"], "-")

    def test_default_expansion(self) -> None:
        parsed = self.parse(
            "export PORT=${EDGE_HTTP_PORT:-19080}\nexport SET=${KNOWN:-fb}\n",
            base={"KNOWN": "known"},
        )
        self.assertEqual(parsed["PORT"], "19080")
        self.assertEqual(parsed["SET"], "known")

    def test_multiline_value(self) -> None:
        source = {"LOCAL_TEST_COMMAND": "npx jest a\nnpx jest b", "X": "y"}
        path = self.tmp / "multi.env"
        write_env_file(path, source)
        self.assertEqual(parse_env_file(path, base_env={}), source)

    def test_legacy_printf_q_format(self) -> None:
        parsed = self.parse(
            "export LOCAL_TEST_COMMAND=$'npx jest a\\nnpx jest b'\n"
            "export SIMPLE=pnpm\\ exec\\ jest\n"
            "export DOLLAR=lit\\ \\$HOME\\ x\n"
        )
        self.assertEqual(parsed["LOCAL_TEST_COMMAND"], "npx jest a\nnpx jest b")
        self.assertEqual(parsed["SIMPLE"], "pnpm exec jest")
        self.assertEqual(parsed["DOLLAR"], "lit $HOME x")

    def test_comments_and_hash_values(self) -> None:
        parsed = self.parse("export A=1 # trailing comment\nexport B='x # kept'\n")
        self.assertEqual(parsed, {"A": "1", "B": "x # kept"})

    def test_command_substitution_fails_loudly(self) -> None:
        with self.assertRaisesRegex(Fail, "command substitution"):
            self.parse("export X=$(cat foo)\n")
        with self.assertRaisesRegex(Fail, "backtick"):
            self.parse("export X=`date`\n")

    def test_unsupported_expansion_fails_loudly(self) -> None:
        with self.assertRaisesRegex(Fail, "unsupported parameter expansion"):
            self.parse("export X=${FOO:?required}\n")

    def test_roundtrip_tricky_values(self) -> None:
        source = {
            "WITH_SPACES": 'pnpm exec jest -t "some test"',
            "WITH_SINGLE": "it's a value",
            "WITH_DOLLAR": "literal $HOME here",
            "EMPTY": "",
            "URL": "http://localhost:18000/graphql?a=b&c=d",
        }
        path = self.tmp / "round.env"
        write_env_file(path, source)
        self.assertEqual(parse_env_file(path, base_env={}), source)

    @unittest.skipUnless(shutil.which("bash"), "bash not available")
    def test_written_files_source_identically_in_bash(self) -> None:
        source = {
            "LOCAL_TEST_COMMAND": "npx jest a\nnpx jest b",
            "WITH_DOLLAR": "literal $HOME here",
            "WITH_SPACES": 'a "b" c',
        }
        path = self.tmp / "bash.env"
        write_env_file(path, source)
        script = (
            f'set -a; source "{path}"; set +a; '
            "python3 -c 'import os,json;print(json.dumps("
            "{k: os.environ[k] for k in (\"LOCAL_TEST_COMMAND\",\"WITH_DOLLAR\",\"WITH_SPACES\")}))'"
        )
        output = subprocess.run(
            ["bash", "-c", script],
            capture_output=True,
            text=True,
            env={"PATH": os.environ["PATH"]},
        )
        self.assertEqual(json.loads(output.stdout), source)


class TestCtxDefaults(unittest.TestCase):
    def test_empty_counts_as_unset(self) -> None:
        ctx = Ctx(env={"LOCAL_PLATFORM_EDGE_CACHE_DIR": "", "BACKEND_HTTP_PORT": ""})
        self.assertTrue(str(ctx.edge_cache_dir()).endswith(".local-platform/cache/edge"))
        ctx.set_default_ports()
        self.assertEqual(ctx.env["BACKEND_HTTP_PORT"], "18000")

    def test_truthy_gates(self) -> None:
        ctx = Ctx(env={"EMPTY": "", "ZERO": "0", "ON": "1"})
        self.assertTrue(ctx.truthy("EMPTY", "1"), "set-but-empty falls back to default")
        self.assertFalse(ctx.truthy("ZERO", "1"))
        self.assertTrue(ctx.truthy("ON"))
        self.assertTrue(ctx.truthy("ABSENT", "1"))
        self.assertFalse(ctx.truthy("ABSENT"))

    def test_is_truthy_spellings(self) -> None:
        for falsy in ("", "0", "false", "FALSE", "no", "No", "off", "OFF"):
            self.assertFalse(is_truthy(falsy), falsy)
        for truthy in ("1", "true", "yes", "on", "anything"):
            self.assertTrue(is_truthy(truthy), truthy)


