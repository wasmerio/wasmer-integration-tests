class TestPackageList(TempDirTest):
    def test_merge_dedupe_and_comments(self) -> None:
        seed = self.write(
            "seed.json",
            json.dumps(
                {
                    "resolved": [
                        {"resolvedName": "wasmer/pkg-a", "resolvedVersion": "1.0.0"},
                        {"resolvedName": "wasmer/pkg-a", "resolvedVersion": "1.0.0"},
                        {"resolvedName": None, "resolvedVersion": "2.0"},
                    ]
                }
            ),
        )
        extra = self.write(
            "extra.txt",
            "# a comment\n"
            "wasmer/pkg-b@=2.0.0  # pinned for tests\n"
            "\n"
            "wasmer/pkg-a@=1.0.0\n",
        )
        output = self.tmp / "resolved.txt"
        packages = _build_package_list(seed, extra, output)
        self.assertEqual(packages, ["wasmer/pkg-a@=1.0.0", "wasmer/pkg-b@=2.0.0"])
        self.assertEqual(
            output.read_text(), "wasmer/pkg-a@=1.0.0\nwasmer/pkg-b@=2.0.0\n"
        )

    def test_missing_inputs_give_empty_list(self) -> None:
        output = self.tmp / "resolved.txt"
        packages = _build_package_list(
            self.tmp / "nope.json", self.tmp / "nope.txt", output
        )
        self.assertEqual(packages, [])


class TestRedaction(unittest.TestCase):
    def test_tokens_redacted(self) -> None:
        raw = (
            "WASMER_TOKEN=wap_secret123\n"
            "export EDGE_SYNC_TOKEN=abc.def\n"
            "OTHER=untouched\n"
        )
        redacted = _REDACT_PATTERN.sub(r"\1<redacted>", raw)
        self.assertNotIn("wap_secret123", redacted)
        self.assertNotIn("abc.def", redacted)
        self.assertIn("WASMER_TOKEN=<redacted>", redacted)
        self.assertIn("OTHER=untouched", redacted)


class TestJwt(TempDirTest):
    @unittest.skipUnless(shutil.which("openssl"), "openssl not available")
    def test_structure_and_signature(self) -> None:
        import base64

        key = self.tmp / "key.pem"
        subprocess.run(
            ["openssl", "genrsa", "-out", str(key), "2048"],
            check=True,
            capture_output=True,
        )
        token = sign_edge_grpc_token(key)
        header_b64, payload_b64, signature_b64 = token.split(".")

        def decode(part: str) -> bytes:
            return base64.urlsafe_b64decode(part + "=" * (-len(part) % 4))

        self.assertEqual(json.loads(decode(header_b64)), {"alg": "RS512", "typ": "JWT"})
        payload = json.loads(decode(payload_b64))
        self.assertEqual(payload["sub"], "wasmerio-backend")
        self.assertEqual(payload["node_api_permissions"], ["all"])
        self.assertEqual(payload["exp"] - payload["iat"], 30 * 24 * 60 * 60)

        # Verify the RS512 signature with openssl against the public key.
        public = self.tmp / "pub.pem"
        subprocess.run(
            ["openssl", "rsa", "-in", str(key), "-pubout", "-out", str(public)],
            check=True,
            capture_output=True,
        )
        signature_file = self.tmp / "sig.bin"
        signature_file.write_bytes(decode(signature_b64))
        verify = subprocess.run(
            [
                "openssl",
                "dgst",
                "-sha512",
                "-verify",
                str(public),
                "-signature",
                str(signature_file),
            ],
            input=f"{header_b64}.{payload_b64}".encode(),
            capture_output=True,
        )
        self.assertEqual(verify.returncode, 0, verify.stderr)


class TestProdTagPattern(unittest.TestCase):
    def test_prod_tags_have_no_channel_suffix(self) -> None:
        from localplatform.resolve import _PROD_TAG_PATTERN

        for prod in ("v2026-07-06_0_ff0e329", "v2026-07-02_3_fdd0152"):
            self.assertIsNotNone(_PROD_TAG_PATTERN.fullmatch(prod), prod)
        for non_prod in (
            "v2026-07-07_3_2af698b_dev",
            "v2026-07-07_2_c8d095c_bugt",
            "v2026-07-07_5_4cfc7b4_dev1",
            "latest",
        ):
            self.assertIsNone(_PROD_TAG_PATTERN.fullmatch(non_prod), non_prod)


class TestDescribeArtifactSource(unittest.TestCase):
    def test_selector_shapes(self) -> None:
        self.assertEqual(
            describe_artifact_source(
                "github-release:wasmerio/edge:v2026-07-07_5_4cfc7b4_dev1:edge"
            ),
            "v2026-07-07_5_4cfc7b4_dev1 (github-release from wasmerio/edge)",
        )
        self.assertEqual(
            describe_artifact_source("github-artifact:wasmerio/backend:backend-image"),
            "backend-image (latest artifact from wasmerio/backend)",
        )
        self.assertEqual(
            describe_artifact_source("artifact:wasmerio/backend:12345:backend-image"),
            "backend-image (artifact from wasmerio/backend run 12345)",
        )
        self.assertEqual(
            describe_artifact_source("path:/opt/edge-binary"), "path:/opt/edge-binary"
        )
        self.assertEqual(
            describe_artifact_source("ghcr.io/wasmerio/backend:v1.2.3"),
            "ghcr.io/wasmerio/backend:v1.2.3",
        )

    def test_backend_description_includes_archive_source(self) -> None:
        ctx = Ctx(
            env={
                "BACKEND_IMAGE_REF": "local-platform-backend:wit_x",
                "BACKEND_IMAGE_SOURCE": "github-release:wasmerio/backend:v2:*image*.tar",
            }
        )
        self.assertEqual(
            describe_backend_version(ctx),
            "local-platform-backend:wit_x "
            "(from v2 (github-release from wasmerio/backend))",
        )
        ctx_pull = Ctx(
            env={"BACKEND_IMAGE_REF": "ghcr.io/x/y:1", "BACKEND_IMAGE_SOURCE": ""}
        )
        self.assertEqual(describe_backend_version(ctx_pull), "ghcr.io/x/y:1")


